       private void carregarTickersNoComboBox() {
           Vector<String> listaDeTickers = new Vector<>();
           
           listaDeTickers.add("Selecione um Ticker");
           
           try{
               List<Ativo> listaFiis = adao.listaFiis();
               
               for(Ativo ativo : listaFiis) {
                   listaDeTickers.add(ativo.getTicker());
               }
               
               DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(listaDeTickers);
               cmbTickers.setModel(model);
           
           }catch(Exception e){
               JOptionPane.showMessageDialog(this, "Erro ao carregar tickers: " + 
                    e.getMessage(), "Erro de Carregamento", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
           }
       }
